hdj = ["焦山楂","焦神曲","焦麦芽"] #消化药 焦三仙
hd1 = ["山楂","焦山楂"] #一仙

hd.append(["焦山楂","焦神曲","焦麦芽"])  #添加列表
hd.append(("焦山楂","焦神曲","焦麦芽"))  #添加元组
hd.append(10)
hd.append("山楂")
print(hd)

print("\n🍒🍒🍒🍒🍒🍒🍒🍒🍒🍒🍒以下是焦三仙改造")
hdj.insert(1,"1,山楂")       #索引1插入元素
hdj.insert(3,(2,"神曲",3,"麦芽")) #索引3插入混合元素
print(hdj)

print("\n🍒🍒🍒🍒🍒🍒🍒🍒🍒🍒🍒以下是焦三仙添加")
hd1.extend(["神曲","焦神曲"]) #添加列表
