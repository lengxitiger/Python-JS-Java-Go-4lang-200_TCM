# ==================== 财务集合模块 ====================
# 资产 <= 总账  # 子集校验：私房钱是否曝光 👀
# 明细 == 汇总  # 相等判断：阴阳账本找不同 🔍
# 总账 >= 分类  # 超集验证：母公司吞并测试 🏢
# 应收 & 应付  # 交集分析：三角债核对 🤝
# 现金 | 存款  # 并集计算：实际资金池 💰
