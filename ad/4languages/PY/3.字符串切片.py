# ==================== 财务切片操作 ====================
# 凭证号[3:7] => 会计期间  # 🧾冷溪虎山_期间提取规则
# 科目代码[:4] => 总账科目  # 📊ERP老兵_科目分级
# 金额字符串[-6:] => 小数部分  # ⚖️虎山系统_精度控制
# 交易流水号[::2] => 校验码  # 🔍冷溪审计_跳位校验

str = ("红花 redflower")
print(str[:]) #全部
print(str[0::]) #全部
print(str[2:2])
print(str[0])
print(str[1:])
print(str[2:6])

print("\n","🔰🔰🔰🔰🔰🔰以下是负切片")
print(str[::-1])
print(str[2:-1])
print(str[-3:])
print(str[-1])
print(str[-1::])
print(str[-1::-1])  #倒序输出

print("\n","-"*20,"以下是步长切片")
print(str[0::3])
print(str[2::2])
print(str[3:9:2]) #3:9切片步长2

print("\n","🔰🔰🔰🔰🔰🔰以下是列表显式切片")

three = ["山楂","神曲","麦芽","焦山楂","焦神曲","焦麦芽"]

print(three[slice(None, None, 3)]) #显式切片
print(three[slice(3, None, None)]) #显式切片
print(three[::3]) #隐式切片

        
        
