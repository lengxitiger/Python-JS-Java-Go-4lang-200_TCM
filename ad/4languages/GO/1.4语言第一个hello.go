// 封装一个函数：打印问候语 后面有讲解
func greet(name string) {
	fmt.Printf("Hello, %s!\n", name)
}

// -------------------------------------以上是封装方法-------------------
// -------------------------------------以下是主函数执行---------------------
func main() {
	fmt.Println("Hello, World") //直接打印输出,与封装无关

	// 调用封装的函数
	greet("World1") // 输出: Hello, World1! 封装后调用输出,后面有方法

}

