package main

import "fmt"

// ==================== 仓储遍历模块 ====================
// 带位置盘点：给每件货物贴坐标签 🏷️
// 临期检测：过期商品自动亮红灯 🚨
// ⚠️冷溪物流：遍历卡顿会堵死传送带

func main() {
	hd := []string{"山楂", "神曲", "麦芽", "谷芽", "鸡矢藤", "鸡内金", "蜘蛛香", "沙棘"} // 消化药列表

	// 索引默认从0开始
	fmt.Println("🗝️🗝️🗝️🗝️🗝️🗝️🗝️🗝️")
	for index, item := range hd {
		fmt.Printf("索引%d-%s\n", index, item)
	}
	fmt.Println("🗝️🗝️🗝️🗝️🗝️🗝️🗝️🗝️")

	// 索引从1开始
	for i := 0; i < len(hd); i++ {
		fmt.Printf("索引%d-%s\n", i+1, hd[i])
	}
	fmt.Println("🗝️🗝️🗝️🗝️🗝️🗝️🗝️🗝️")

	if contains(hd, "山楂") { // 条件判断
		// 遍历值
		for _, value := range hd {
			fmt.Println(value)
		}
		// 遍历索引
		for i := 0; i < len(hd); i++ {
			fmt.Println(i)
		}
	}
	//防疲劳警示
	HushanTiter10(10)
}

// Go 没有内置的 contains 函数，需要手动实现
func contains(slice []string, item string) bool {
	for _, s := range slice {
		if s == item {
			return true
		}
	}
	return false
}

