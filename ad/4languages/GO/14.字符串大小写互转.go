func swapCase(s string) string {
	result := []rune(s)
	for i, c := range result {
		if unicode.IsUpper(c) {
			result[i] = unicode.ToLower(c)
		} else if unicode.IsLower(c) {
			result[i] = unicode.ToUpper(c)
		}
	}
	return string(result)
}

